s = str(input('Введите строку: '))
print('Третий символ строки:', s[2])
print('Предпоследний символ строки:', s[-2])
print('Первые пять символов строки:', s[0:4])
print('Вся строка, кроме последних двух символов:', s[:-2])
print('Все символы с чётными индексами:', s[::2])
print('Все символы с нечётными индексами:', s[1::2])
print('Все символы в обратном порядке: ', s[::-1])
print('Все символы строки через один в обратном порядке, начиная с последнего', s[::-2])
print('Длина строки:', len(s))
